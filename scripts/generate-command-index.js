import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const SRC_DIR = path.join(__dirname, '../src');
const COMMANDS_DIR = path.join(SRC_DIR, 'modules/commands');
const FEATURES_DIR = path.join(SRC_DIR, 'features');
const INDEX_FILE = path.join(COMMANDS_DIR, 'index.ts');

const EXCLUSIONS = ['index.ts', 'commandManager.ts'];

function getCommandFiles(dir) {
    if (!fs.existsSync(dir)) return [];
    return fs.readdirSync(dir)
        .filter(file => file.endsWith('.ts') && !EXCLUSIONS.includes(file))
        .map(file => ({
            name: file,
            path: path.join(dir, file)
        }));
}

function generate() {
    console.log('Generating command index...');

    // 1. Get commands from modules/commands
    const legacyCommands = getCommandFiles(COMMANDS_DIR).map(f => ({
        ...f,
        relativePath: `./${path.basename(f.name, '.ts')}.js`
    }));

    // 2. Get commands from features/**/commands
    let featureCommands = [];
    if (fs.existsSync(FEATURES_DIR)) {
        const features = fs.readdirSync(FEATURES_DIR);
        features.forEach(feature => {
            const cmdDir = path.join(FEATURES_DIR, feature, 'commands');
            const cmds = getCommandFiles(cmdDir);
            // Calculate relative path from src/modules/commands to src/features/feature/commands
            // src/modules/commands -> src/features/feature/commands
            // ../../features/feature/commands
            cmds.forEach(c => {
                featureCommands.push({
                    ...c,
                    relativePath: `../../features/${feature}/commands/${path.basename(c.name, '.ts')}.js`
                });
            });
        });
    }

    const allCommands = [...legacyCommands, ...featureCommands];
    console.log(`Found ${allCommands.length} command files.`);

    const imports = allCommands
        .map((cmd) => {
            const base = path.basename(cmd.name, '.ts');
            const safeBase = base.replace(/[^a-zA-Z0-9]/g, '');
            // Use feature name prefix to ensure unique variable names
            const featureMatch = cmd.relativePath.match(/features\/([^/]+)\//);
            const featureName = featureMatch ? featureMatch[1] : 'legacy';
            const safeFeature = featureName.replace(/[^a-zA-Z0-9]/g, '');
            const name = 'cmd' + safeFeature.charAt(0).toUpperCase() + safeFeature.slice(1) + safeBase.charAt(0).toUpperCase() + safeBase.slice(1);

            cmd.varName = name;
            return `import ${name} from '${cmd.relativePath}';`;
        })
        .join('\n');

    const list = allCommands
        .map((cmd) => cmd.varName)
        .join(',\n        ');

    const content = `// Auto-generated by scripts/generate-command-index.js
/* eslint-disable */
import { commandManager, CustomCommand } from './commandManager.js';

${imports}

export function loadCommands() {
    const commands = [
        ${list}
    ];

    commands.forEach(cmdModule => {
        if (Array.isArray(cmdModule)) {
            cmdModule.forEach(cmd => commandManager.register(cmd as CustomCommand));
        } else if (cmdModule) {
            commandManager.register(cmdModule as CustomCommand);
        }
    });
}
`;

    fs.writeFileSync(INDEX_FILE, content);
    console.log(`Command index generated at: ${INDEX_FILE}`);
}

generate();
