import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const COMMANDS_DIR = path.join(__dirname, '../src/modules/commands');
const INDEX_FILE = path.join(COMMANDS_DIR, 'index.ts');

const EXCLUSIONS = ['index.ts', 'commandManager.ts'];

function generate() {
    if (!fs.existsSync(COMMANDS_DIR)) {
        console.error('Commands directory not found!');
        process.exit(1);
    }

    const files = fs.readdirSync(COMMANDS_DIR);
    const commandFiles = files.filter(file =>
        file.endsWith('.ts') && !EXCLUSIONS.includes(file)
    );

    const imports = commandFiles.map(file => {
        // Create a safe variable name from filename
        const name = 'cmd_' + path.basename(file, '.ts').replace(/[^a-zA-Z0-9]/g, '_');
        return `import ${name} from './${path.basename(file, '.ts')}.js';`;
    }).join('\n');

    const list = commandFiles.map(file => {
        return 'cmd_' + path.basename(file, '.ts').replace(/[^a-zA-Z0-9]/g, '_');
    }).join(',\n        ');

    const content = `// Auto-generated by scripts/generate-command-index.js
import { commandManager, CustomCommand } from './commandManager.js';

${imports}

export function loadCommands() {
    const commands = [
        ${list}
    ];

    commands.forEach(cmdModule => {
        if (Array.isArray(cmdModule)) {
            cmdModule.forEach(cmd => commandManager.register(cmd as CustomCommand));
        } else if (cmdModule) {
            commandManager.register(cmdModule as CustomCommand);
        }
    });
}
`;

    fs.writeFileSync(INDEX_FILE, content);
    console.log(`Generated command index with ${commandFiles.length} commands.`);
}

generate();
