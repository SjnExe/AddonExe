import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const COMMANDS_DIR = path.join(__dirname, '../src/modules/commands');
const INDEX_FILE = path.join(COMMANDS_DIR, 'index.ts');

const EXCLUSIONS = ['index.ts', 'commandManager.ts'];

function generate() {
    if (!fs.existsSync(COMMANDS_DIR)) {
        console.error('Commands directory not found!');
        process.exit(1);
    }

    const files = fs.readdirSync(COMMANDS_DIR);
    const commandFiles = files.filter((file) => file.endsWith('.ts') && !EXCLUSIONS.includes(file));

    const imports = commandFiles
        .map((file) => {
            // Create a safe variable name from filename (camelCase)
            const base = path.basename(file, '.ts');
            const safeBase = base.replace(/[^a-zA-Z0-9]/g, '');
            const name = 'cmd' + safeBase.charAt(0).toUpperCase() + safeBase.slice(1);
            return `import ${name} from './${base}.js';`;
        })
        .join('\n');

    const list = commandFiles
        .map((file) => {
            const base = path.basename(file, '.ts');
            const safeBase = base.replace(/[^a-zA-Z0-9]/g, '');
            return 'cmd' + safeBase.charAt(0).toUpperCase() + safeBase.slice(1);
        })
        .join(',\n        ');

    const content = `// Auto-generated by scripts/generate-command-index.js
/* eslint-disable */
import { commandManager, CustomCommand } from './commandManager.js';

${imports}

export function loadCommands() {
    const commands = [
        ${list}
    ];

    commands.forEach(cmdModule => {
        if (Array.isArray(cmdModule)) {
            cmdModule.forEach(cmd => commandManager.register(cmd as CustomCommand));
        } else if (cmdModule) {
            commandManager.register(cmdModule as CustomCommand);
        }
    });
}
`;

    fs.writeFileSync(INDEX_FILE, content);
}

generate();
